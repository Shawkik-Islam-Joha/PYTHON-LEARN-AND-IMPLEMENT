import math
import cmath          # For complex numbers 

print(math.sqrt(13))  # Prints approximately 3.605551275463989
print(math.pi)
print(math.e)
print(math.inf)       # Prints inf
print(math.nan)       # Prints not a number
print(math.floor(3.6))    # Round down
print(math.ceil(3.5))     # Round up
print(math.trunc(4.5))    # Deletes decimal part
print(math.pow(5,2))      # Power 5^2
print(math.log(100,10))   # Log 100 base 10
print(math.log(math.e))   # By default,base e
print(math.sin(math.radians(90)))    # Trigonometric functions take input in radians
print(math.degrees(math.pi/2))       # Converts to degree
print(math.sinh(0))       # sin hyperbolic 
print(math.factorial(5))  # 5! = 120
print(math.gcd(30,45))
print(math.isclose(10.5,10.51))       # Tolerance check
print(math.comb(5,4))
print(math.perm(10,7))

